    //ESTE ES EL METODO QUE REALIZA LAS OPERACIONES AL PRECIONAR EL BOTON = Y ES DONDE SE LLAMAN LOS METODOS
    private void igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_igualActionPerformed
        //Pongo la formula en la pestaña de formula original
        FormulaText3.setText(FormulaText.getText());
        //se guarda la formula en una variable para simplemente 
        //llamar la variable y no llamar a todo el metodo get del JTextField
        String formula = FormulaText.getText();
        
        
        
        //se cambia las operaciones por los simbolos
        formula = formula.replace("sqrt", "?");
        formula = formula.replace("cos", "#");
        formula = formula.replace("sen", "%");
        formula = formula.replace("tan", "&");
        //esta parte es para pedir los valores de las letras y remplazarlos
        for (int i = 0; i < formula.length(); i++) {
            if (Character.isLetter(formula.charAt(i))) {
                String letraValor = JOptionPane.showInputDialog("Ingrese el valor de " + formula.charAt(i));
                formula = formula.replace(String.valueOf(formula.charAt(i)), letraValor);
            }
        }
        //aqui se hace el balanceo
        npi.balanceo(formula, pila);
        try {
            //aqui se llena la cola de la formula original
            npi.formulaOriginal(formula, colaFormulaOriginal);
        } catch (colaVaciaException ex) {
        }
        if (pila.empty() == false) {
            //en el caso de que no esté balanceada la formula
            FormulaText.setText("La formula no está balanceada");
        } else {
            try {
                //la formula postfija se hace con el siguiente metodo, aquí la guardé en una variable de tipo clsCola
                clsCola resultado = npi.formulaPostfija(colaFormulaOriginal);
                //poner la formula postfija en su respectiva pestana
                FormulaText4.setText(resultado.imprimir());
                //aquí evaluamos la formula y mostramos el resultado 
                FormulaText.setText(npi.evaluarFormula(resultado));
            } catch (colaVaciaException ex) {
                System.out.println(ex.getMessage());
            }
        }
    }//GEN-LAST:event_igualActionPerformed

